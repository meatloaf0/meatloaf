package com.meatloaf.excel;

public class UserData {
	int uno;
	String uid;
	String uname;
	String upw;
	int ugrant;
	String utel;
	
	public UserData(int uno, String uid, String name, String upw, int ugrant, String utel) {
		this.uno = uno;
		this.uid = uid;
		this.uname = name;
		this.upw = upw;
		this.ugrant = ugrant;
		this.utel = utel;
	}

}
